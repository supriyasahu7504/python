# Topic 1: Arithmetic Operators
a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Remainder:", a % b)
print("Power:", a ** b)


# Topic 2: Comparison Operators
a = 20
b = 10

print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)


# Topic 3: Assignment Operators
x = 10

x += 5
print(x)   # 15

x -= 2
print(x)   # 13

x *= 3
print(x)   # 39

x /= 3
print(x)   # 13.0


# Topic 4: Operator Precedence
result = 10 + 2 * 5
print(result)

result = (10 + 2) * 5
print(result)
